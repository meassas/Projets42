#include "libft.h"

void	ft_putendl(char const *s)
{
	char *s1;

	s1 = (char*)s;
	if (!s)
		return ;
	if (*s)
	{
		ft_putstr((char*)s1++);
		ft_putchar('\n');
	}
}
